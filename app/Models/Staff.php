{
    use HasFactory;
    protected $table = 'staff';

    public $timestamps = true;
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name',
        'image',
        'quote',
        'position'
    ];
}
